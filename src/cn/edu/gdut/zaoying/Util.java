public class Util {
    public static int phraseColor(String str){
        return Integer.parseInt(str,16);
    }
    public static String toFirstLetterUpperCase(String src){
        return src.substring(0,1).toUpperCase()+src.substring(1);
    }
    public static String toFirstLetterLowerCase(String src){
        return src.substring(0,1).toLowerCase()+src.substring(1);
    }
}
